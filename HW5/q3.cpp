// global constants
const int WIDTH = 78, HEIGHT = 20;

// show * if there is a living cell
void display_board(int board[HEIGHT + 2][WIDTH + 2]) {
    for (int i = 1; i < (HEIGHT + 1); i++) {
        for (int j = 1; j < (WIDTH + 1); j++) {
            if (board[i][j] == 1) {
                // living cell
                cout << "*";
            }
            else {
                // dead cell
                cout << " ";
            }
        }
        cout << "\n";
    }
}

// determines birth and death of cells
void new_generation(int board[HEIGHT + 2][WIDTH + 2]) {
    int temp_board[HEIGHT + 2][WIDTH + 2];
    int neighbours;
    void copy_board(int board1[HEIGHT + 2][WIDTH + 2], int board2[HEIGHT + 2][WIDTH + 2]);

    for (int i = 0; i < (HEIGHT + 2); i++) {
        for (int j = 0; j < (WIDTH + 2); j++) {
            if (i == 0 || j == 0 || i == (HEIGHT + 2) || j == (WIDTH + 2)) {
                temp_board[i][j] = 0;
            }
            else {
                // count living neighbors
                neighbours = board[i - 1][j - 1] + board[i - 1][j] + board[i - 1][j + 1]
                             + board[i][j - 1] + board[i][j + 1] + board[i + 1][j - 1]
                             + board[i + 1][j] + board[i + 1][j + 1];

                if (board[i][j] == 1) {
                    if (neighbours < 2 || neighbours > 3) {
                        //cell dies due to loneliness or overcrowding
                        temp_board[i][j] = 0;
                    }
                    else {
                        temp_board[i][j] = 1;
                    }
                }

                else {
                    if (neighbours == 3) {
                        // new cell
                        temp_board[i][j] = 1;
                    }
                    else {
                        temp_board[i][j] = 0;
                    }
                }
            }
        }
    }

    copy_board(temp_board, board);
}

// carry progress
void copy_board(int board1[HEIGHT + 2][WIDTH + 2], int board2[HEIGHT + 2][WIDTH + 2]) {
    for (int i = 0; i < (HEIGHT + 2); i++) {
        for (int j = 0; j < WIDTH + 2; j++) {
            board2[i][j] = board1[i][j];
        }
    }
}

